     */
    public function store(Request $request)
    {
        $request->validate([
            'share_name'=>'required',
            'share_price'=> 'required|numeric',
            'share_qty' => 'required|date'
          ]);
          $employee = new Employee([ // localhost error: Class 'App\Http\Controllers\Employee' not found
